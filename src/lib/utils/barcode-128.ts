/**
 * Encodes text into a Code 128 binary string.
 */
export function encodeCode128(text: string): string {
    const startSymbol = 104; // Start Code B
    let checksum = startSymbol;
    let binary = START_CODE;

    for (let i = 0; i < text.length; i++) {
        const charCode = text.charCodeAt(i);
        const symbol = charCode - 32;
        if (symbol < 0 || symbol > 102) continue; // Skip unsupported chars
        binary += CODE128_PATTERNS[symbol];
        checksum += symbol * (i + 1);
    }

    const checkDigit = checksum % 103;
    binary += CODE128_PATTERNS[checkDigit];
    binary += STOP_CODE;

    return binary;
}

/**
 * Returns an array of bars (x, width) for an SVG representation.
 */
export function generateBarcodeBars(text: string) {
    const binary = encodeCode128(text);
    const bars: { x: number; width: number }[] = [];
    let currentX = 0;
    
    for (let i = 0; i < binary.length; i++) {
        if (binary[i] === '1') {
            let width = 1;
            while (i + 1 < binary.length && binary[i + 1] === '1') {
                width++;
                i++;
            }
            bars.push({ x: currentX, width });
            currentX += width;
        } else {
            currentX++;
        }
    }
    
    return { bars, totalWidth: currentX };
}
