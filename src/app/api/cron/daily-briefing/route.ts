/**
 * Find all orgs with at least one active playbook.
 */
async function getOrgsWithActivePlaybooks(): Promise<string[]> {
    const db = getAdminFirestore();

    // Query tenants that have a slackChannel configured (proxy for "active pilot")
    const tenantsSnap = await db
        .collection('tenants')
        .where('slackChannel', '!=', null)
        .select('slackChannel')
        .limit(50)
        .get();

    return tenantsSnap.docs.map(doc => doc.id);
}
