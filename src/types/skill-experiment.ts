/**
 * Firestore collection: skill_experiments
 *
 * Every champion/challenger run is durably recorded here.
 * This is the experiment ledger — a queryable research asset.
 *
 * Key indexes (add to Firestore):
 *   - skill_name + timestamp DESC          (list runs for a skill)
 *   - skill_name + promotion_stage + composite_score DESC  (find best candidates)
 *   - skill_name + keep_or_discard + timestamp DESC        (regression analysis)
 */

// ─────────────────────────────────────────────────────────────────────────────
// Per-criterion result stored in the ledger
// ─────────────────────────────────────────────────────────────────────────────

export interface LedgerCriterionResult {
    criterionId: string;
    criterionName: string;
    category: string;
    isGate: boolean;
    passed: boolean;
    weight: number;
    reasoning?: string;
}

// ─────────────────────────────────────────────────────────────────────────────
// Per-case result stored in the ledger
// ─────────────────────────────────────────────────────────────────────────────

export interface LedgerCaseResult {
    caseId: string;
    gatesPassed: boolean;
    score: number;
    maxScore: number;
    passRate: number;
    criteriaResults: LedgerCriterionResult[];
}

// ─────────────────────────────────────────────────────────────────────────────
// Aggregated run metrics stored for each version in the ledger
// ─────────────────────────────────────────────────────────────────────────────

export interface LedgerRunMetrics {
    totalCases: number;
    gatesPassedCount: number;
    gatesFailedCount: number;
    compositeScore: number;
    anyGateFailure: boolean;

    /** Pass rate per criterion ID */
    criteriaPassRates: Record<string, number>;

    /** Cost of judge API calls for this run (USD estimate) */
    judgeCostUsd: number;

    /** Wall-clock ms for the full run */
    runDurationMs: number;
}

// ─────────────────────────────────────────────────────────────────────────────
// Main experiment record
// ─────────────────────────────────────────────────────────────────────────────

export interface SkillExperiment {
    /** Auto-generated Firestore document ID */
    experiment_id: string;

    /** Skill identifier (e.g., "domain.product-description") */
    skill_name: string;

    /** Semver of the champion instructions at experiment start */
    parent_version: string;

    /** Semver of the challenger being tested */
    candidate_version: string;

    /** One-line summary of what changed (generated by mutation engine) */
    mutation_summary: string;

    /**
     * Unified diff of SKILL.md champion → challenger.
     * Stored as a string for auditability.
     */
    mutation_diff: string;

    /**
     * Strategy used to generate this candidate:
     * - "llm_rewrite"       — LLM rewrote a section for clarity
     * - "example_reorder"   — examples reordered or swapped
     * - "compression"       — repetitive language removed
     * - "tool_hint_update"  — tool-use hints revised
     * - "anti_hallucination" — hallucination guidance strengthened
     * - "human_patch"       — manually authored candidate
     * - "replay"            — replay of a previously successful edit
     */
    mutation_strategy:
        | 'llm_rewrite'
        | 'example_reorder'
        | 'compression'
        | 'tool_hint_update'
        | 'anti_hallucination'
        | 'human_patch'
        | 'replay';

    /** Version of the eval_spec.json used (e.g., "1.0") */
    eval_spec_version: string;

    /** Version of the eval dataset used */
    dataset_version: string;

    /**
     * Which dataset was used for this run:
     * - "dev"        — development iteration set
     * - "holdout"    — promotion-gate set (used only for promotion decisions)
     * - "regression" — historical failure cases
     */
    dataset_type: 'dev' | 'holdout' | 'regression';

    /** Model used for generation (champion instructions run through this model) */
    generation_model: string;

    /** Model used for judge-type criteria */
    judge_model: string;

    /** Champion run metrics */
    champion_metrics: LedgerRunMetrics;

    /** Challenger run metrics */
    challenger_metrics: LedgerRunMetrics;

    /** challenger composite score minus champion composite score */
    score_delta: number;

    /** Challenger compliance failure rate (0-1) */
    compliance_fail_rate: number;

    /** Whether the challenger meets the minimum promotion delta */
    meets_promotion_delta: boolean;

    /**
     * Final decision for this experiment:
     * - "keep"    — challenger is better; candidate for promotion
     * - "discard" — challenger lost or failed gates
     * - "pending" — awaiting holdout run or human review
     */
    keep_or_discard: 'keep' | 'discard' | 'pending';

    /**
     * Promotion stage reached:
     * - "offline"   — eval-only, no live traffic
     * - "shadow"    — running against shadow traffic
     * - "limited"   — limited internal rollout
     * - "promoted"  — full champion promotion
     * - "rolled_back" — promoted then reverted
     */
    promotion_stage: 'offline' | 'shadow' | 'limited' | 'promoted' | 'rolled_back';

    /** Reasons for rejection (empty if promoted) */
    rejection_reasons: string[];

    /** Whether human review was required or requested */
    human_review_required: boolean;

    /** UID of reviewer who approved/rejected (null if auto-decided) */
    reviewed_by: string | null;

    /** Firestore Timestamp (stored as ISO string in TypeScript) */
    timestamp: string;

    /** Date string YYYY-MM-DD for daily aggregation queries */
    _date: string;
}

// ─────────────────────────────────────────────────────────────────────────────
// Telemetry-sourced production signal (for feedback ingestor)
// Pulled from agent_telemetry to inform next optimization cycle
// ─────────────────────────────────────────────────────────────────────────────

export interface SkillProductionSignal {
    skill_name: string;
    period_start: string;
    period_end: string;

    /** % of outputs accepted without user edits */
    accepted_without_edit_rate: number;

    /** Average edit distance on outputs that were edited */
    avg_edit_distance: number;

    /** % of outputs rejected outright */
    rejection_rate: number;

    /** % of outputs that triggered a compliance flag */
    compliance_flag_rate: number;

    /** % of tool calls that succeeded on first try */
    tool_success_rate: number;

    /** Average retry count per invocation */
    avg_retry_count: number;

    /** Average dead-end loop count per invocation */
    avg_dead_end_loops: number;

    /** Average end-to-end latency in ms */
    avg_latency_ms: number;

    /** Average cost per invocation in USD */
    avg_cost_usd: number;

    /** Number of invocations in the period */
    sample_count: number;
}
