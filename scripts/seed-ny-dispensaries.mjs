// ── Seed function ───────────────────────────────────────────────────────────

function slugify(text) {
  return text.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

async function seed() {
  console.log(`\n🌿 BakedBot NY Dispensary Seed`);
  console.log(`   Mode: ${DRY_RUN ? 'DRY RUN' : 'LIVE'} | Update existing: ${UPDATE_EXISTING}`);
  console.log(`   Seeding ${NY_DISPENSARIES.length} dispensaries...\n`);

  const col = db.collection('retailers');
  const crmCol = db.collection('crm_dispensaries');
  let created = 0, skipped = 0, updated = 0, crmCreated = 0;
  const now = Date.now();

  for (const d of NY_DISPENSARIES) {
    const slug = d.slug || slugify(d.name);
    const docRef = col.doc(slug);

    const existing = await docRef.get();
    if (existing.exists && !UPDATE_EXISTING) {
      process.stdout.write(`  ⏭  ${d.name} — already exists`);
      skipped++;
    } else {
      const record = {
        name: d.name,
        slug,
        address: d.address || '',
        city: d.city,
        state: d.state,
        zip: d.zip,
        ...(d.lat && d.lng ? { lat: d.lat, lng: d.lng } : {}),
        ...(d.website ? { website: d.website } : {}),
        ...(d.phone ? { phone: d.phone } : {}),
        ...(d.licenseNumber ? { licenseNumber: d.licenseNumber } : {}),
        licenseType: d.licenseType || 'AUCD',
        type: 'dispensary',
        status: 'active',
        source: 'ny-ocm-seed',
        updatedAt: new Date(),
        ...(existing.exists ? {} : { createdAt: new Date() }),
      };

      if (DRY_RUN) {
        process.stdout.write(`  ✅ [DRY RUN] ${d.name} (${d.city}) — would ${existing.exists ? 'update' : 'create'}`);
      } else if (existing.exists) {
        await docRef.update(record);
        process.stdout.write(`  🔄 Updated retailers: ${d.name} (${d.city})`);
        updated++;
      } else {
        await docRef.set(record);
        process.stdout.write(`  ✅ Created retailers: ${d.name} (${d.city})`);
        created++;
      }
    }

    // ── Also sync to crm_dispensaries (the outreach pipeline source) ──────
    const crmQuery = await crmCol.where('slug', '==', slug).limit(1).get();
    if (crmQuery.empty) {
      const crmRecord = {
        name: d.name,
        slug,
        address: d.address || '',
        city: d.city,
        state: d.state,
        zip: d.zip || '',
        ...(d.website ? { website: d.website } : {}),
        ...(d.phone ? { phone: d.phone } : {}),
        source: 'import',
        claimStatus: 'unclaimed',
        retailerId: slug,
        discoveredAt: now,
        updatedAt: now,
        createdAt: now,
      };

      if (!DRY_RUN) {
        const crmRef = crmCol.doc(slug);
        await crmRef.set(crmRecord, { merge: true });
        crmCreated++;
        console.log(` → CRM ✅`);
      } else {
        console.log(` → CRM [DRY RUN]`);
      }
    } else {
      console.log(` → CRM already exists`);
    }
  }

  console.log(`\n📊 Retailers: ${created} created, ${updated} updated, ${skipped} skipped`);
  console.log(`📊 CRM dispensaries: ${crmCreated} new entries added`);
  console.log(`\nNext steps:`);
  console.log(`  1. Run CRM queue sync: node scripts/sync-retailers-to-crm.mjs --state=NY`);
  console.log(`  2. Run logo fetcher: node scripts/fetch-dispensary-logos.mjs --state=NY --dry-run`);
  console.log(`  3. Verify at: https://bakedbot.ai/dispensaries?state=NY\n`);
}

await seed();
